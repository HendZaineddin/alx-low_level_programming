#include "3-calc.h"
/**
 * op_add - function add two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function sub two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function mul two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function div two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function mod two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
